function applyLang(lang) {
  if (!TRANSLATIONS[lang]) return
  localStorage.setItem('el_lang', lang)
  const t = TRANSLATIONS[lang]

  // Update all data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n')
    if (t[key] !== undefined) el.innerHTML = t[key]
  })

  // Update placeholders
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph')
    if (t[key] !== undefined) el.placeholder = t[key]
  })

  // Update lang buttons active state
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang)
  })

  // Update select options
  const starsAny = document.getElementById('h-stars')
  if (starsAny) starsAny.options[0].text = t['search.stars.any'] || starsAny.options[0].text

  const fClass = document.getElementById('f-class')
  if (fClass) {
    const opts = ['search.class.economy', 'search.class.business', 'search.class.first']
    opts.forEach((k, i) => { if (fClass.options[i] && t[k]) fClass.options[i].text = t[k] })
  }
}

// Apply saved language on load
document.addEventListener('DOMContentLoaded', () => {
  const saved = localStorage.getItem('el_lang') || 'ru'
  if (saved !== 'ru') applyLang(saved)
})
